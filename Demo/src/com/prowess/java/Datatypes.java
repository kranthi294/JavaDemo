package com.prowess.java;

public class Datatypes {

	public static void main(String[] args) {
		byte b = 10;
		short s = 20;
		int i = 30;
		long l = 40l;
		float f = 50f;
		char c = 'A';
		double d = 60d;
		boolean boo = false;
		String str = "Kranthi Buddha";
		System.out.println(b+" | "+s+" | "+i+" | "+l+" | "+f+" | "+c+" | "+d+" | "+boo+" | "+str);
	}

}
